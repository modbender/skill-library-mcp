class Reporter {
  constructor(config) {
    this.config = config;
  }

  async generate(cryptoAPI, stockAPI) {
    const date = new Date().toLocaleDateString('zh-CN', {
      year: 'numeric',
      month: 'long',
      day: 'numeric',
      weekday: 'long'
    });

    let report = `# рҹ“Ҡ жҠ•иө„з»„еҗҲж—ҘжҠҘ\n\n`;
    report += `**з”ҹжҲҗж—¶й—ҙ**: ${date}  \n`;
    report += `**иҙ§еёҒ**: ${this.config.currency || 'USD'}  \n\n`;
    report += `---\n\n`;

    // Crypto section
    if (this.config.watchlist.crypto?.length > 0) {
      report += `## рҹ’Һ еҠ еҜҶиҙ§еёҒ\n\n`;
      
      for (const symbol of this.config.watchlist.crypto) {
        try {
          const data = await cryptoAPI.getPrice(symbol.toLowerCase());
          const price = data?.usd || 'N/A';
          const change24h = data?.usd_24h_change;
          
          report += `### ${symbol.toUpperCase()}\n`;
          report += `- **д»·ж ј**: $${price.toLocaleString()}\n`;
          if (change24h !== undefined) {
            const emoji = change24h >= 0 ? 'рҹ“Ҳ' : 'рҹ“ү';
            report += `- **24h еҸҳеҢ–**: ${emoji} ${change24h > 0 ? '+' : ''}${change24h.toFixed(2)}%\n`;
          }
          report += `\n`;
        } catch (e) {
          report += `### ${symbol.toUpperCase()}\n- ж•°жҚ®иҺ·еҸ–еӨұиҙҘ\n\n`;
        }
      }
    }

    // Stock section
    if (this.config.watchlist.stocks?.length > 0) {
      report += `## рҹ“Ҳ иӮЎзҘЁ\n\n`;
      
      for (const symbol of this.config.watchlist.stocks) {
        try {
          const data = await stockAPI.getQuote(symbol);
          const emoji = data.change >= 0 ? 'рҹ“Ҳ' : 'рҹ“ү';
          
          report += `### ${symbol}\n`;
          report += `- **д»·ж ј**: $${data.price.toLocaleString()}\n`;
          report += `- **ж¶Ёи·Ң**: ${emoji} $${data.change > 0 ? '+' : ''}${data.change.toFixed(2)} (${data.changePercent > 0 ? '+' : ''}${data.changePercent.toFixed(2)}%)\n`;
          report += `- **иҙ§еёҒ**: ${data.currency}\n\n`;
        } catch (e) {
          report += `### ${symbol}\n- ж•°жҚ®иҺ·еҸ–еӨұиҙҘ\n\n`;
        }
      }
    }

    // Active alerts
    if (this.config.alerts?.length > 0) {
      report += `## рҹ”” жҙ»и·ғиӯҰжҠҘ\n\n`;
      const activeAlerts = this.config.alerts.filter(a => !a.triggered);
      
      if (activeAlerts.length === 0) {
        report += `жҡӮж— жҙ»и·ғиӯҰжҠҘ\n\n`;
      } else {
        activeAlerts.forEach(alert => {
          report += `- **${alert.symbol}**: `;
          if (alert.above) report += `зӘҒз ҙ $${alert.above.toLocaleString()}`;
          if (alert.below) report += `и·Ңз ҙ $${alert.below.toLocaleString()}`;
          if (alert.percent) report += `жіўеҠЁи¶…иҝҮ ${alert.percent}%`;
          report += `\n`;
        });
        report += `\n`;
      }
    }

    report += `---\n\n`;
    report += `*Generated by Finance Watcher*  \n`;
    report += `*Data from CoinGecko & Yahoo Finance*  \n`;
    report += `*Powered by OpenClaw* рҹҗҫ`;

    return report;
  }
}

module.exports = Reporter;
